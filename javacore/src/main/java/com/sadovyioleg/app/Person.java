package com.sadovyioleg.app;


public class Person {

    private int age;
    private int workingTime;
    private boolean workFlag;
    private boolean feedbackFlag;
    private int salary;
    public Person() {
    }
    public int getAge() {
        return this.age;    }
    public void setAge(int age) {
        this.age = age;    }
    public int getWorkingTime() {
        return this.workingTime;    }
    public void setWorkingTime(int workingTime) {
        this.workingTime = workingTime;    }
    public boolean isWorkFlag() {
        return this.workFlag;    }
    public void setWorkFlag(boolean workFlag) {
        this.workFlag = workFlag;    }
    public boolean isFeedbackFlag() {
        return this.feedbackFlag;    }
    public void setFeedbackFlag(boolean feedbackFlag) {
        this.feedbackFlag = feedbackFlag;    }
    public int getSalary(int i) {
        return this.salary;    }
    public void setSalary(int salary) {
        this.salary = salary;
    }





}
